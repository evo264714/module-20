const color = 'yellow';
if(color === 'green'){
    console.log('This is green.');
}
else if (color === 'blue'){
    console.log('This is blue.');
}
else if (color === 'red'){
    console.log('This is red.');
}
else if (color === 'white'){
    console.log('This is white.');
}
else if (color === 'yellow'){
    console.log('This is yellow.');
}
else{
    console.log('This is black');
}

//! SWITCH CASE

switch(color){
    case 'green':
        console.log('This is green.')
        break;
    case 'blue':
        console.log('This is blue.')
        break;
    case 'white':
        console.log('This is white.')
        break;
    case 'red':
        console.log('This is red.')
        break;
    case 'yellow':
        console.log('This is yellow.')
        break;
    default:
        console.log('This is green.')

}
